        </p>

        <p class="text-sm mb-4">
            The plan includes developing a magnificent temple and supporting facilities envisioned by Srila Prabhupada near the birthplace on land free from lease restrictions. A 4.5-acre plot, located just 300 meters from the current temple, is currently being pursued for acquisition.
        </p>

        <p class="text-sm mb-4">
            At the same time, efforts will continue to negotiate with the management of the Birnagar Dvadasa Temple to secure sevaiteship of the existing birthplace temple.
        </p>

        <p class="text-sm mb-4">
