    <div class="mb-10">
        <h4 class="text-lg font-semibold mb-4">Whereas</h4>

        <ul class="list-disc pl-6 space-y-3 text-sm">
            <li>Birnagar is the birthplace of Srila Bhaktivinoda Thakura and holds significant historical and spiritual importance for devotees.</li>

            <li>Srila Prabhupada expressed his deep desire to develop Birnagar to broadcast the glories of Srila Bhaktivinoda Thakura, the eminent Acarya of this Gaudiya Sampradaya, to the world.</li>

            <li>Srila Prabhupada had a profound desire to honor Srila Bhaktivinoda Thakura by developing his birthplace into a significant center for spiritual education and pilgrimage.</li>

            <li>The Birnagar project is one of the most important projects for every Gaudiya Vaisnava, especially for our movement.</li>

            <li>Srila Prabhupada requested the Bhaktivinoda Gosthi and Srila Lalita Prasada Thakura to permit us to render this service, but the proposal was not accepted.</li>

            <li>Even after 52 years, the Bhaktivinoda Gosthi has not agreed to the proposal for developing the Janma-sthana.</li>

            <li>Through the Bhaktivedanta Swami Charity Trust, renovation and construction work was carried out for the Birnagar Dvadasa Siva Mandir, but sevaiteship has not been granted.</li>

            <li>If sevaiteship is not provided, long-term maintenance of a large temple on their property will be difficult.</li>

            <li>The Bhaktivinoda Gosthi is not ready to provide land on lease or any other arrangement at present.</li>

            <li>As per the desire of Srila Prabhupada, a magnificent temple along with educational and devotional facilities should be established.</li>

            <li>ISKCON should acquire land near the birthplace and develop a memorial and temple for Srila Bhaktivinoda Thakura.</li>

            <li>Due to the scale of the project, it should be directly supported and managed by ISKCON.</li>
        </ul>
    </div>

    <!-- Resolution Section -->
    <div>
        <h4 class="text-lg font-semibold mb-4">Therefore, it is resolved that:</h4>

        <p class="text-sm mb-4">
